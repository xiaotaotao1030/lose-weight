function summarizeMealPlan(meals) {
  return meals.reduce(
    (total, meal) => ({
      calories: total.calories + meal.calories,
      proteinG: total.proteinG + meal.proteinG,
      carbG: total.carbG + meal.carbG,
      fatG: total.fatG + meal.fatG,
    }),
    { calories: 0, proteinG: 0, carbG: 0, fatG: 0 }
  );
}
